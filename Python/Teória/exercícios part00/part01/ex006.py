
# Calcúlo de média de notas
n1 = float(input("Digite a sua nota do 1º trimestre: "))
n2 = float(input("Digite a sua nota do 2º trimestre: "))
n3 = float(input("Digite a sua nota do 3º trimestre: "))
n4 = float(input("Digite a sua nota do 4º trimestre: "))
m = n1 + n2 + n3 + n4
mt = m / 4
print("A média de suas notas é: ", mt)
